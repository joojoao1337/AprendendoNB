    public void selectTab() {
        String valLin = "";

        int posLin = tabPes.getSelectedRow();

        for (int col = 0; col < tabPes.getColumnCount(); col++) {
            valLin += tabPes.getModel().getValueAt(posLin, col).toString();
            
            if(col+1 < tabPes.getColumnCount()){
                valLin += " - ";
            }
        }

        JOptionPane.showMessageDialog(
                null,
                "O valor da linha é: " + valLin,
                "Captura de linha de tabela",
                1
        );
    }

    public void consTabPes() {
        DefaultTableModel tabModel = (DefaultTableModel) tabPes.getModel();
        int posLin = 0;
        tabModel.setRowCount(posLin);

        for (Pessoa p : gp.getBdPes()) {
            tabModel.insertRow(posLin, new Object[]{p.getCpf(), p.getNome()});
            posLin++;
        }
    }

    public void delPesCpf() {
        try {
            p1 = new Pessoa();
            p1.setCpf(Integer.parseInt(cxCpf.getText()));

            p1 = gp.delPesCpf(p1);

            if (p1 == null) {
                JOptionPane.showMessageDialog(
                        null,
                        "Pessoa deletada",
                        "DelPes",
                        JOptionPane.INFORMATION_MESSAGE
                );
            } else {
                JOptionPane.showMessageDialog(
                        null,
                        "Pessoa não encontrada",
                        "DelPes",
                        JOptionPane.ERROR_MESSAGE
                );
            }

        } catch (NumberFormatException nfe) {
            erroException();
        }
    }

    public void altPesCpf() {
        try {
            p1 = new Pessoa();
            p1.setCpf(Integer.parseInt(cxCpf.getText()));

            p1 = gp.atualizaPesCpf(p1);

            if (p1 != null) {
                cxCpf.setText(Integer.toString(p1.getCpf()));
                cxNome.setText(p1.getNome());

                JOptionPane.showMessageDialog(
                        null,
                        "Pessoa encontrada - Confira os dados",
                        "AltPes",
                        JOptionPane.INFORMATION_MESSAGE
                );
            } else {
                JOptionPane.showMessageDialog(
                        null,
                        "Pessoa não encontrada ",
                        "AltPes",
                        JOptionPane.ERROR_MESSAGE
                );
                cxCpf.setText("");
                cxCpf.requestFocus();
            }

        } catch (NumberFormatException nfe) {
            erroException();
        }
    }

    public void consPes() {

        try {
            p1 = new Pessoa();
            p1.setCpf(Integer.parseInt(cxCpf.getText()));

            p1 = gp.consPesCpf(p1);

            if (p1 != null) {

                cxNome.setText(p1.getNome());

                JOptionPane.showMessageDialog(
                        null,
                        "Pessoa encontrada ",
                        "ConsPes",
                        JOptionPane.INFORMATION_MESSAGE
                );
            } else {
                JOptionPane.showMessageDialog(
                        null,
                        "Não encontrada pessoa com esse cpf",
                        "ConsPes",
                        JOptionPane.ERROR_MESSAGE
                );
                cxCpf.setText("");
                cxCpf.requestFocus();
            }
        } catch (NumberFormatException nfe) {
            erroException();
        }
    }

    public void insPes() {

        try {
            p1 = new Pessoa();

            p1.setCpf(Integer.parseInt(cxCpf.getText()));
            p1.setNome(cxNome.getText());

            p1 = gp.insPes(p1);
            if (p1 != null) {
                JOptionPane.showMessageDialog(
                        null,
                        "Pessoa cadastrada com sucesso!",
                        "Cadastro de Pessoas",
                        JOptionPane.INFORMATION_MESSAGE
                );
                limpar();
            } else {
                JOptionPane.showMessageDialog(
                        null,
                        "Já existe uma pessoa com este CPF!",
                        "Cadastro de Pessoas",
                        JOptionPane.ERROR_MESSAGE
                );
                cxCpf.setText("");
                cxCpf.requestFocus();
            }
        } catch (NumberFormatException nfe) {
            erroException();
        }
    }

    public void erroException() {
        JOptionPane.showMessageDialog(
                null,
                "NumberFormatException",
                "Cadastro de Pessoas",
                JOptionPane.ERROR_MESSAGE
        );
        cxCpf.setText("");
        cxCpf.requestFocus();
    }

    public void limpar() {
        cxCpf.setText("");
        cxNome.setText("");
        cxCpf.requestFocus();
    }

